let fahrenheit = 32

// calculate celsius and store in celsius variable
// print it
let celsius = (fahrenheit - 32) * 5/9
console.log(fahrenheit + ' degrees F equals ' + celsius + ' degrees C.')

// calculate kelvin and store in kelvin variable
// print it
let kelvin = (fahrenheit + 459.67) * 5/9
console.log(fahrenheit + ' degrees F equals ' + kelvin + ' degrees K.')